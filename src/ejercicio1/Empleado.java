package ejercicio1;

public class Empleado {
	
	// PROPIEDADES
	private int id;
	private String nombre;
	private int edad;
	
	// CONSTRUCTOR VACIO
	public Empleado() {
	}
	
	public Empleado(String nombre, int edad) {
		
	}

	//SETTERS Y GETTERS
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}

	

	
}
